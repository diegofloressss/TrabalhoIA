public class Nodo {
    int x;  // Coordenada X do nó
    int y;  // Coordenada Y do nó

    // Construtor para inicializar o nó com as coordenadas X e Y
    public Nodo(int x, int y) {
        super();  // Chama o construtor da classe pai (no caso, Object)
        this.x = x;  // Inicializa a coordenada X
        this.y = y;  // Inicializa a coordenada Y
    }
}
